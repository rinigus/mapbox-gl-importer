CREATE OR REPLACE FUNCTION island_rank(area REAL) RETURNS INT AS $$
    SELECT CASE
        WHEN area < 10000000 THEN 11
        WHEN area BETWEEN  1000000 AND 15000000 THEN 10
        WHEN area BETWEEN 15000000 AND 40000000 THEN 9
        WHEN area > 40000000 THEN 8
        ELSE 12
    END;
$$ LANGUAGE SQL IMMUTABLE STRICT;
